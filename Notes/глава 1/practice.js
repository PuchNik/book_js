// Примеры математических операторов
let a = 10;
let b = 5;

console.log(a + b); 
console.log(a - b); 
console.log(a * b); 
console.log(a / b); 
console.log(a % b); 
console.log(a ** b); 


let isTrue = true;
let isFalse = false;

console.log(isTrue && isFalse); 
console.log(isTrue || isFalse); 
console.log(isTrue || isFalse || isTrue);

//Пример специальных чисел
console.log(NaN);
console.log(Infinity);
console.log(-Infinity);
